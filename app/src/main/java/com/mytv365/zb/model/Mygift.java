public class Mygift {

    private String giftEffect;
    private String giftName;
    private String giftPrice;
    private String giftLogo;
    private String id;


    public String getGiftEffect() {
        return giftEffect;
    }

    public void setGiftEffect(String giftEffect) {
        this.giftEffect = giftEffect;
    }

    public String getGiftName() {
        return giftName;
    }

    public void setGiftName(String giftName) {
        this.giftName = giftName;
    }

    public String getGiftPrice() {
        return giftPrice;
    }

    public void setGiftPrice(String giftPrice) {
        this.giftPrice = giftPrice;
    }

    public String getGiftLogo() {
        return giftLogo;
    }

    public void setGiftLogo(String giftLogo) {
        this.giftLogo = giftLogo;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
